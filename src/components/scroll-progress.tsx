'use client'

import React from 'react'
import { motion, useScroll, useSpring } from 'framer-motion'

export const ScrollProgress = () => {
    const { scrollYProgress } = useScroll()
    const scaleX = useSpring(scrollYProgress, {
        stiffness: 100,
        damping: 30,
        restDelta: 0.001
    })

    return (
        <motion.div
            className="fixed top-0 left-0 right-0 h-1.5 bg-gradient-to-r from-rose-400 via-champagne-400 to-rose-400 z-[9999] origin-left"
            style={{ scaleX }}
        />
    )
}
